document.getElementById("imageButton").onclick = function() {
    var dropdown = document.getElementById("imageDropdown");
    // Set the width of the dropdown to match the image width
    dropdown.style.width = this.offsetWidth + 'px'; // Match the image width
    dropdown.style.display = dropdown.style.display === "none" ? "block" : "none";
};

